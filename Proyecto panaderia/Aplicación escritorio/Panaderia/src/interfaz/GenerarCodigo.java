    private void crea39(String dato) throws IOException {
        cb.setCodeType(new Code39());
        cb.setCode(dato);
        cb.setCheckDigit(true);

        BufferedImage b = cb.draw(new BufferedImage(200, 100, BufferedImage.TYPE_INT_RGB));
        ruta = "C:\\Users\\hugo_\\Desktop\\codigos de barra\\" + dato + ".png";
        file = new File(ruta);
        ImageIO.write(b, "png", file);

    }

    private void establecerImagen() {
        Image foto = Toolkit.getDefaultToolkit().getImage(ruta);
        this.jLabel4.setIcon(new ImageIcon(foto.getScaledInstance(300, 150, 0)));
    }

